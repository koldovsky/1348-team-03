//Yurii Komaniak
function move(position, roll) {
    return position + roll * 2
}

//Oleksandr Krasylnykov
function move(position, roll) {
    return position = position + roll * 2;
}

//Nikita Zemlianskyi
function move(position, roll) {
    return Number(position) + 2 * Number(roll);
}